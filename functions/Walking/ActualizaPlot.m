function ActualizaPlot
global WlkTab
global Robot

w_T_o=[Robot.w_R_o Robot.w_p_o; 0 0 0 1];
mlab_T_o=w_T_o;

x_o=mlab_T_o(1,4);
y_o=mlab_T_o(2,4);
z_o=mlab_T_o(3,4);

BLD=bld_DK;

set(WlkTab.Plt_RightArm1,'XData',[x_o BLD.T_obd01(1,4) ],'YData',[y_o BLD.T_obd01(2,4) ],'ZData',[z_o BLD.T_obd01(3,4) ]);
%%Right Arm
set(WlkTab.Plt_RightArm2,'XData',[BLD.T_obd01(1,4) BLD.T_obd02(1,4) BLD.T_obd03(1,4) BLD.T_obd05(1,4) BLD.T_obd06(1,4)],...
       'YData',[BLD.T_obd01(2,4) BLD.T_obd02(2,4) BLD.T_obd03(2,4) BLD.T_obd05(2,4) BLD.T_obd06(2,4)],...
       'ZData',[BLD.T_obd01(3,4) BLD.T_obd02(3,4) BLD.T_obd03(3,4) BLD.T_obd05(3,4) BLD.T_obd06(3,4)]);
%%Left Arm
set(WlkTab.Plt_LeftArm1,  'XData',[x_o BLD.T_obi01(1,4) ],'YData',[y_o BLD.T_obi01(2,4) ],'ZData',[z_o BLD.T_obi01(3,4) ]);
set(WlkTab.Plt_LeftArm2,  'XData',[BLD.T_obi01(1,4) BLD.T_obi02(1,4) BLD.T_obi03(1,4) BLD.T_obi05(1,4) BLD.T_obi06(1,4)],...
        'YData',[BLD.T_obi01(2,4) BLD.T_obi02(2,4) BLD.T_obi03(2,4) BLD.T_obi05(2,4) BLD.T_obi06(2,4)],...
        'ZData',[BLD.T_obi01(3,4) BLD.T_obi02(3,4) BLD.T_obi03(3,4) BLD.T_obi05(3,4) BLD.T_obi06(3,4)]);
%%Head
w_p_cabeza=Robot.w_p_o+Robot.w_R_o*[0 0 50/10].';
w_T_cabeza=[Robot.w_R_o w_p_cabeza; 0 0 0 1];
mlab_T_cabeza=w_T_cabeza;
set(WlkTab.Plt_Head,'XData',[x_o mlab_T_cabeza(1,4)],...
        'YData',[y_o mlab_T_cabeza(2,4)],...
        'ZData',[z_o mlab_T_cabeza(3,4)]);
%%Right leg
set(WlkTab.Plt_RightLeg1,  'XData',[x_o BLD.T_opd00a(1,4) BLD.T_opd01(1,4) ],...
        'YData',[y_o BLD.T_opd00a(2,4) BLD.T_opd01(2,4) ],...
        'ZData',[z_o BLD.T_opd00a(3,4) BLD.T_opd01(3,4) ]);
set(WlkTab.Plt_RightLeg2,  'XData',[BLD.T_opd01(1,4) BLD.T_opd03(1,4) BLD.T_opd05(1,4) BLD.T_opd05a(1,4) BLD.T_opd07(1,4) BLD.T_opd07a(1,4) BLD.T_opd09(1,4) BLD.T_opd12(1,4)],...
        'YData',[BLD.T_opd01(2,4) BLD.T_opd03(2,4) BLD.T_opd05(2,4) BLD.T_opd05a(2,4) BLD.T_opd07(2,4) BLD.T_opd07a(2,4) BLD.T_opd09(2,4) BLD.T_opd12(2,4)],...
        'ZData',[BLD.T_opd01(3,4) BLD.T_opd03(3,4) BLD.T_opd05(3,4) BLD.T_opd05a(3,4) BLD.T_opd07(3,4) BLD.T_opd07a(3,4) BLD.T_opd09(3,4) BLD.T_opd12(3,4)]);
%%Left leg
set(WlkTab.Plt_LeftLeg1,  'XData',[x_o BLD.T_opi00a(1,4) BLD.T_opi01(1,4) ],...
        'YData',[y_o BLD.T_opi00a(2,4) BLD.T_opi01(2,4) ],...
        'ZData',[z_o BLD.T_opi00a(3,4) BLD.T_opi01(3,4) ])
set(WlkTab.Plt_LeftLeg2,  'XData',[BLD.T_opi01(1,4) BLD.T_opi03(1,4) BLD.T_opi05(1,4) BLD.T_opi05a(1,4) BLD.T_opi07(1,4) BLD.T_opi07a(1,4) BLD.T_opi09(1,4) BLD.T_opi12(1,4)],...
        'YData',[BLD.T_opi01(2,4) BLD.T_opi03(2,4) BLD.T_opi05(2,4) BLD.T_opi05a(2,4) BLD.T_opi07(2,4) BLD.T_opi07a(2,4) BLD.T_opi09(2,4) BLD.T_opi12(2,4)],...
        'ZData',[BLD.T_opi01(3,4) BLD.T_opi03(3,4) BLD.T_opi05(3,4) BLD.T_opi05a(3,4) BLD.T_opi07(3,4) BLD.T_opi07a(3,4) BLD.T_opi09(3,4) BLD.T_opi12(3,4)]);
%%Right foot
set(WlkTab.Plt_lnft1, 'XData',[BLD.T_opdb(1,4) BLD.T_opdf(1,4)],...
      'YData',[BLD.T_opdb(2,4) BLD.T_opdf(2,4)],...
      'ZData',[BLD.T_opdb(3,4) BLD.T_opdf(3,4)]);
set(WlkTab.Plt_lnft2, 'XData',[BLD.T_opdP1(1,4) BLD.T_opdP2(1,4)],...
      'YData',[BLD.T_opdP1(2,4) BLD.T_opdP2(2,4)],...
      'ZData',[BLD.T_opdP1(3,4) BLD.T_opdP2(3,4)]);
set(WlkTab.Plt_lnft3, 'XData',[BLD.T_opdP3(1,4) BLD.T_opdP4(1,4)],...
      'YData',[BLD.T_opdP3(2,4) BLD.T_opdP4(2,4)],...
      'ZData',[BLD.T_opdP3(3,4) BLD.T_opdP4(3,4)]);
%%Left foot
set(WlkTab.Plt_lnft4, 'XData',[BLD.T_opib(1,4) BLD.T_opif(1,4)],...
      'YData',[BLD.T_opib(2,4) BLD.T_opif(2,4)],...
      'ZData',[BLD.T_opib(3,4) BLD.T_opif(3,4)]);
set(WlkTab.Plt_lnft5, 'XData',[BLD.T_opiP1(1,4) BLD.T_opiP2(1,4)],...
      'YData',[BLD.T_opiP1(2,4) BLD.T_opiP2(2,4)],...
      'ZData',[BLD.T_opiP1(3,4) BLD.T_opiP2(3,4)]);
set(WlkTab.Plt_lnft6, 'XData',[BLD.T_opiP3(1,4) BLD.T_opiP4(1,4)],...
      'YData',[BLD.T_opiP3(2,4) BLD.T_opiP4(2,4)],...
      'ZData',[BLD.T_opiP3(3,4) BLD.T_opiP4(3,4)]);
drawnow;